package com.example;

import java.util.Date;

public class Hotel {
    private int HotelId;
    private String HotelName;
    private Date HotelStartDateAndTime;
    private Date HotelEndDateAndTime;
    private String HotelGroundName;
    public int getHotelId() {
        return HotelId;
    }
    public void setHotelId(int HotelId) {
        this.HotelId = HotelId;
    }
    public String getHotelName() {
        return HotelName;
    }
    public void setHotelName(String HotelName) {
        this.HotelName = HotelName;
    }
    public Date getHotelStartDateAndTime() {
        return HotelStartDateAndTime;
    }
    public void setHotelStartDateAndTime(Date HotelStartDateAndTime) {
        this.HotelStartDateAndTime = HotelStartDateAndTime;
    }
    public Date getHotelEndDateAndTime() {
        return HotelEndDateAndTime;
    }
    public void setHotelEndDateAndTime(Date HotelEndDateAndTime) {
        this.HotelEndDateAndTime = HotelEndDateAndTime;
    }
    public String getHotelGroundName() {
        return HotelGroundName;
    }
    public void setHotelGroundName(String HotelGroundName) {
        this.HotelGroundName = HotelGroundName;
    }
}



